#include "../../libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	dst_len;
	size_t	src_len;

	i = 0;
	dst_len = ft_strlen(dst);
	src_len = ft_strlen(src);
	if (size <= 0)
		return (size + src_len);
	while (src[i] && dst_len + i < size - 1)
	{
		dst[dst_len + i] = src[i];
		i++;
	}
	dst[dst_len + i] = '\0';
	if (size < dst_len)
		return (size + src_len);
	return (dst_len + src_len);
}
/* concatène deux strings src et dst
size correspond à la taille de la dst
la src est concaténée à la fin de la dst
fin de la nouvelle dst avec \0
renvoie la longueur de la dst qu'il a essayé de créer */